-- Create hs_codes table
CREATE TABLE IF NOT EXISTS hs_codes (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    code TEXT NOT NULL UNIQUE,
    description TEXT NOT NULL,
    chapter TEXT,
    heading TEXT,
    subheading TEXT,
    duty_rate NUMERIC DEFAULT 0,
    vat_rate NUMERIC DEFAULT 0,
    excise_rate NUMERIC DEFAULT 0,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Indexes for hs_codes
CREATE UNIQUE INDEX IF NOT EXISTS idx_hs_codes_code ON hs_codes(code);
CREATE INDEX IF NOT EXISTS idx_hs_codes_description ON hs_codes(description);
CREATE INDEX IF NOT EXISTS idx_hs_codes_chapter ON hs_codes(chapter);
CREATE INDEX IF NOT EXISTS idx_hs_codes_heading ON hs_codes(heading);

